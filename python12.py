#Implement a program with lamda functions, for finding the area of circle, triangle, square.

area = lambda r : 3.14*r*2
print("area of the circle:")
print(area(6))

areat = lambda h,b : h*b
print("area of the triangle:")
print(areat(2,4))

areas = lambda a : a*a
print("area of the square:")
print(areas(4))
